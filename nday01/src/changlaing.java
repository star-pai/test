/*常量：在程序运行过程中，其值是不可以发生改变的量
 * 分类：1.字符串常量（用双引号括的内容）
 * 2.整数常量（88）  3.小数常量（2.2） 4.字符常量（用单引号括的内容）
 * 5.布尔常量（true，false)   6.空常量（null)
 *   
 *  总结：只有空常量不能直接输出。
 *
 * */
public class changlaing {
	public static void main(String [] args){
		//字符串 常量
		System .out.println("hello world");
		System .out.println("--------");
		//整数常量
		System .out.println(666);
		System .out.println("--------");
		//小数常量
		System .out.println(666.6);
		System .out.println("--------");
		//字符常量
		System .out.println('a');
		System .out.println("--------");
		//布尔常量
		System .out.println(true);
		System .out.println("--------");
		//空 常量
		 //System .out.println(null);
		
		
		
		
		
	}

}
